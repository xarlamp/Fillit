#include "libft.h"

void	ft_strclr(char *s)
{
	size_t i;

	if (s == 0)
		return ;
	i = 0;
	while (s[i] != 0)
	{
		if (i > 0)
			s[i - 1] = 0;
		i++;
	}
	s[i - 1] = 0;
}
