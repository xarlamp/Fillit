#include "libft.h"

int	ft_atoi(const char *str)
{
	int neg;
	int res;

	neg = 1;
	res = 0;
	while (*str == ' ' || ((*str > 8) && (*str < 14)))
		str++;
	if (*str == '-')
	{
		neg = -1;
		str++;
	}
	else if (*str == '+')
	{
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		res = res * 10 + (*str - '0');
		str++;
	}
	return (neg * res);
}
