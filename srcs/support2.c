void	ft_free_map(char **map)
{
	int i;
	i = 0;
	while (map[i] != 0)
	{
		free(map[i]);
		i++;
	}
	free(map);
	ft_error();
}

void	ft_error(void)
{
	ft_putstr("Error!\n");
	exit (0);
}
